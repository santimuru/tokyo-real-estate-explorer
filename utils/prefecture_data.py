# Maps our name_en to the exact 'nam' property in dataofjapan/land japan.geojson
_GEO_NAME_OVERRIDES = {
    "Hokkaido": "Hokkai Do",
    "Tokyo":    "Tokyo To",
    "Osaka":    "Osaka Fu",
    "Kyoto":    "Kyoto Fu",
}

def _geo_name(name_en: str) -> str:
    return _GEO_NAME_OVERRIDES.get(name_en, name_en + " Ken")


def get_prefecture(code: int) -> dict:
    return PREFECTURES[code]


def get_all_as_df() -> "pd.DataFrame":
    import pandas as pd
    rows = []
    for code, p in PREFECTURES.items():
        rows.append({"code": code, "name_geo": _geo_name(p["name_en"]), **p})
    return pd.DataFrame(rows)
